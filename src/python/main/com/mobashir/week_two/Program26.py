# Compare the ages of two people and display all comparison results.

age1 = int(input("Enter age of first person: "))
age2 = int(input("Enter age of second person: "))

# Comparisons
print(f"Is {age1} == {age2} ==> {age1==age2}")
print(f"Is {age1} != {age2} ==> {age1!=age2}")
print(f"Is {age1} > {age2} ==> {age1>age2}")
print(f"Is {age1} >= {age2} ==> {age1>=age2}")
print(f"Is {age1} < {age2} ==> {age1<age2}")
print(f"Is {age1} <= {age2} ==> {age1<=age2}")