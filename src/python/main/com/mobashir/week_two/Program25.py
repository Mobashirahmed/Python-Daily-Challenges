# Display the results of all comparison operators between two numbers.
var1 = int(input("Enter a number: "))
var2 = int(input("Enter another number: "))

# Comparisons
print(f"Is {var1} == {var2} ==> {var1 == var2}")
print(f"Is {var1} != {var2} ==> {var1 != var2}")
print(f"Is {var1} > {var2} ==> {var1 > var2}")
print(f"Is {var1} >= {var2} ==> {var1 >= var2}")
print(f"Is {var1} < {var2} ==> {var1 < var2}")
print(f"Is {var1} <= {var2} ==> {var1 <= var2}")