var = 10

# Addition assignment
var += 6
print(var)

# Subtraction assignment
var -= 5
print(var)

# Multiplication assignment
var *= 7
print(var)

# Floating-point Division assignment
var /= 3
print(var)

# Exponential assignment
var **= 2
print(var)

# Floor Division assignment
var //= 9
print(var)

# Modulus assignment
var %= 7
print(var)