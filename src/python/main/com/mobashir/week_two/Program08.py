# Write a program that performs all arithmetic operations on two user-input numbers.

# Take user inputs
a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))

# Perform Arithmetic operations
print(f"{a} + {b} = {a+b}")
print(f"{a} - {b} = {a-b}")
print(f"{a} * {b} = {a*b}")
print(f"{a} / {b} = {a/b}")
print(f"{a} // {b} = {a//b}")
print(f"{a} % {b} = {a%b}")
print(f"{a} ** {b} = {a**b}")
